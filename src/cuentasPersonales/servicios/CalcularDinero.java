package cuentasPersonales.servicios;

public class CalcularDinero {
    public CalcularDinero(){}

    public int sumarDinero(int dineroActual, int dineroASumar){
        return dineroActual + dineroASumar;
    }
    public int restarDinero(int dineroActual, int dineroARestar){
        return dineroActual - dineroARestar;
    }
}
